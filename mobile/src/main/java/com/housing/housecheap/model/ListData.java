package com.housing.housecheap.model;

public class ListData {

	String title;
	int imgResIdGray, imgResIdBlack;
	String booleanVal;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getImgResIdGray() {
		return imgResIdGray;
	}

	public void setImgResIdGray(int imgResIdGray) {
		this.imgResIdGray = imgResIdGray;
	}

	public int getImgResIdBlack() {
		return imgResIdBlack;
	}

	public void setImgResIdBlack(int imgResIdBlack) {
		this.imgResIdBlack = imgResIdBlack;
	}

	public String getBooleanVal() {
		return booleanVal;
	}

	public void setBooleanVal(String booleanVal) {
		this.booleanVal = booleanVal;
	}

}
